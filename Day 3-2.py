def countTrees( map, move_right, move_down ):
    trees = 0

    width = len(map[0])
    height = len(map)
    
    row = 0
    col = 0
    while row < height-move_down:
        col += move_right
        if col >= width: col = col - width       
        row += move_down
       
        chk_ = map[row][col]
        if chk_ == '#': trees+=1

    return trees


